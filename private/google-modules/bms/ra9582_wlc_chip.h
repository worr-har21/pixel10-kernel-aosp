#define RA9582_CHIP_H_

#include  <linux/bits.h>

#define RA9582_CHIP_ID					0x9582

#define RA9582_CHIP_ID_REG				0x00
#define RA9582_CHIP_REVISION_REG			0x02
#define RA9582_CUSTOMER_ID_REG				0x03
#define RA9582_FIRMWARE_MAJOR_REG			0x04
#define RA9582_FIRMWARE_MINOR_REG			0x06
#define RA9582_FIRMWARE_DATE_REG			0xC4
#define RA9582_FW_DATE_SIZE				12
#define RA9582_FIRMWARE_TIME_REG			0xD4
#define RA9582_FW_TIME_SIZE				8
#define RA9582_WPC_MANUFACTURER_REG			0x1E
#define RA9582_STATUS_REG				0x34
#define RA9582_INTERRUPT_REG				0x36
#define RA9582_INTERRUPT_ENABLE_REG			0x38
#define RA9582_INTERRUPT_CLEAR_REG			0x3A
#define RA9582_RAW_IOUT_ADC_REG				0x44
#define RA9582_CHARGE_STATUS_REG			0x4E
#define RA9582_END_OF_POWER_TRANSFER_REG		0x4F
#define RA9582_VOUT_VALUE_REG				0x50
#define RA9582_VOUT_SET_REG				0x52
#define RA9582_VRECT_VALUE_REG				0x54
#define RA9582_IOUT_VALUE_REG				0x58
#define RA9582_DIE_TEMP_REG				0x5A
#define RA9582_OP_FREQ_REG				0x5C
#define RA9582_VRECT_TARGET_REG				0xE4
#define RA9582_FOD_COEF_START_REG			0x84

#define RA9582_ALERT_STATUS_REG				0x130
#define RA9582_LOAD_STEP_REG				0x133
#define RA9582_EXTENDED_GUARANTEED_LOAD_POWER_REG	0x134
#define RA9582_CALIBRATION_REG				0x139
#define RA9582_START_CALIBRATION_VALUE			0x3
#define RA9582_CALIBRATION_STATUS_BIT			BIT(0)
#define RA9582_CALIBRATION_COMPLETE_BIT			BIT(7)

#define RA9582_NUM_FODS					16
#define RA9582_CLOAK_REASON_REG				0x138
#define RA9582_CLOAK_REASON_TX_INITATED_VAL		6

#define RA9582_CALIBRATION_TEST_REG			0xDE
#define RA9582_CALIBRATION_CHECK_RETRIES		10
#define RA9582_CALIBRATION_CHECK_IOUT_COUNT		32


/* Interrupt Bits */
#define RA9582_INTERRUPT_SADT_ERR_BIT		BIT(0)
#define RA9582_INTERRUPT_OVER_TEMP_BIT		BIT(2)
#define RA9582_INTERRUPT_OVER_CURR_BIT		BIT(3)
#define RA9582_INTERRUPT_OVER_VOLT_BIT		BIT(4)
#define RA9582_INTERRUPT_MODE_BIT		BIT(5)
#define RA9582_INTERRUPT_VRECT_BIT		BIT(6)
#define RA9582_INTERRUPT_VOUT_BIT		BIT(7)
#define RA9582_INTERRUPT_SADT_SENT_BIT		BIT(8)
#define RA9582_INTERRUPT_SADT_RCVD_BIT		BIT(9)
#define RA9582_INTERRUPT_LOAD_INCREASE_BIT	BIT(11)
#define RA9582_INTERRUPT_LOAD_DECREASE_BIT	BIT(10)
#define RA9582_INTERRUPT_PTC_BIT		BIT(14)
#define RA9582_INTERRUPT_PROP_RCVD_BIT		BIT(15)

/* System Commands */
#define RA9582_SYSTEM_COMMAND_REG		0x62
#define RA9582_COMMAND_SEND_RETRIES		5
#define RA9582_COMMAND_SEND_ADT			BIT(9)
#define RA9582_COMMAND_STORE_SETTING		BIT(7)
#define RA9582_COMMAND_CLOAK_TOGGLE		BIT(6)
#define RA9582_COMMAND_CLEAR_IRQ		BIT(5)
#define RA9582_COMMAND_SEND_CHG_STATUS		BIT(4)
#define RA9582_COMMAND_SEND_EOP			BIT(3)
#define RA9582_COMMAND_MCU_RESET		BIT(2)
#define RA9582_COMMAND_LDO_TOGGLE		BIT(1)
#define RA9582_COMMAND_SEND_PROP		BIT(0)

#define RA9582_SEND_SIZE_REG			0x94
#define RA9582_SEND_BUF_REG			0x1004
#define RA9582_RECV_SIZE_REG			0x98
#define RA9582_RECV_BUF_REG			0x1004
#define RA9582_DATA_BUF_SIZE			0x2000
#define RA9582_ADT_ERR_REG			0xA1
#define RA9582_EDS_RESET_REG			0x17A
#define RA9582_EDS_STATUS_REG			0x9C
#define RA9582_EDS_TX_BUSY_BIT			BIT(0)
#define RA9582_EDS_RX_BUSY_BIT			BIT(1)
#define RA9582_EDS_STATUS_BUSY_BITS		(RA9582_EDS_TX_BUSY_BIT | RA9582_EDS_RX_BUSY_BIT)

/* Sys Op modes */
#define RA9582_SYS_OP_MODE_REG			0x3F
#define RA9582_SYS_OP_MODE_AC_MISSING		0x00
#define RA9582_SYS_OP_MODE_WPC_BPP		0x20
#define RA9582_SYS_OP_MODE_CALIBRATION		0x22
#define RA9582_SYS_OP_MODE_WPC_EPP		0x40
#define RA9582_SYS_OP_MODE_WPC_CLOAK		0xB0
#define RA9582_SYS_OP_MODE_WPC_MPP		0xE0
#define RA9582_SYS_OP_MODE_WPC_MPP_RESTRICTED	0xF0

#endif  // RA9582_CHIP_H_
