static inline u32 gxp_pf_state_to_power_rate(u32 pf_state)
{
	switch (pf_state) {
	case 0:
		return AUR_MAX_FREQUENCY_RATE;
	case 1:
		return AUR_PERCENT_FREQUENCY_95_RATE;
	case 2:
		return AUR_PERCENT_FREQUENCY_90_RATE;
	case 3:
		return AUR_PERCENT_FREQUENCY_85_RATE;
	case 4:
		return AUR_PERCENT_FREQUENCY_80_RATE;
	case 5:
		return AUR_PERCENT_FREQUENCY_70_RATE;
	case 6:
		return AUR_PERCENT_FREQUENCY_60_RATE;
	case 7:
		return AUR_PERCENT_FREQUENCY_50_RATE;
	case 8:
		return AUR_PERCENT_FREQUENCY_45_RATE;
	case 9:
		return AUR_PERCENT_FREQUENCY_40_RATE;
	case 10:
		return AUR_PERCENT_FREQUENCY_35_RATE;
	case 11:
		return AUR_PERCENT_FREQUENCY_25_RATE;
	case 12:
		return AUR_PERCENT_FREQUENCY_20_RATE;
	case 13:
		return AUR_PERCENT_FREQUENCY_15_RATE;
	case 14:
		return AUR_PERCENT_FREQUENCY_10_RATE;
	case 15:
		return AUR_PERCENT_FREQUENCY_5_RATE;
	default:
		return AUR_MAX_FREQUENCY_RATE;
	}
}

