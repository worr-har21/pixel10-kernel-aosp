/*
 * dp_tune_attr_grps holds main/pre/post files generated by DP_TUNE_PE_GROUP so the phy driver
 * can access attribute groups programmatically instead of by name
 */
static struct attribute_group *dp_tune_attr_grps[DP_TUNE_NUM_RATES][DP_TUNE_NUM_VS][DP_TUNE_NUM_PE];

/*
 * dp_tune_coeff_type used in dp_tune_attribute so the correct gphy->dp_tune_<coeff> map is
 * selected
 */
enum dp_tune_coeff_type {
	DP_TUNE_MAIN,
	DP_TUNE_PRE,
	DP_TUNE_POST,
};

/*
 * dp_tune_kobj_data and derivatives for pe/vs/rate are used to generate sysfs files in the
 * correct hierarchy
 */
struct dp_tune_pe_kobj_data {
	struct kobject *kobj;
};

struct dp_tune_vs_kobj_data {
	struct kobject *kobj;
	struct dp_tune_pe_kobj_data pe[DP_TUNE_NUM_PE];
};

struct dp_tune_rate_kobj_data {
	struct kobject *kobj;
	struct dp_tune_vs_kobj_data vs[DP_TUNE_NUM_VS];
};

struct dp_tune_kobj_data {
	struct kobject *kobj;
	struct dp_tune_rate_kobj_data rates[DP_TUNE_NUM_RATES];
};

/*
 * dp_tune_attribute holds info necessary for reading/writing main/pre/post coefficients
 * from gphy->dp_tune_<coeff> in dp_tune_coeff_show/store functions without the need to
 * generate a new one for each sysfs attribute.
 */
struct dp_tune_attribute {
	struct kobj_attribute attr;
	enum dp_tune_coeff_type coeff;
	u8 rate;
	u8 vs;
	u8 pe;
};

static ssize_t dp_tune_coeff_store(struct kobject *kobj, struct kobj_attribute *attr,
	const char *buf, size_t count);

static ssize_t dp_tune_coeff_show(struct kobject *kobj, struct kobj_attribute *attr, char *buf);

/*
 * DP_TUNE_PE_GROUP macro generates sysfs kobj attributes for a rate/vs/pe combo's main/pre/post
 * coefficients, and then combines them into a common attribute group.
 */
#define DP_TUNE_PE_GROUP(_rate, _vs, _pe)							\
static struct dp_tune_attribute dp_tune_rate_##_rate##_vs_##_vs##_pe_##_pe##_main =		\
	{ __ATTR(main, 0644, dp_tune_coeff_show, dp_tune_coeff_store),				\
	 DP_TUNE_MAIN, _rate, _vs, _pe };							\
static struct dp_tune_attribute dp_tune_rate_##_rate##_vs_##_vs##_pe_##_pe##_pre =		\
	{ __ATTR(pre, 0644, dp_tune_coeff_show, dp_tune_coeff_store),				\
	 DP_TUNE_PRE, _rate, _vs, _pe };							\
static struct dp_tune_attribute dp_tune_rate_##_rate##_vs_##_vs##_pe_##_pe##_post =		\
	{ __ATTR(post, 0644, dp_tune_coeff_show, dp_tune_coeff_store),				\
	 DP_TUNE_POST, _rate, _vs, _pe };							\
												\
static struct attribute *dp_tune_rate_##_rate##_vs_##_vs##_pe_##_pe##_attrs[] = {		\
	&dp_tune_rate_##_rate##_vs_##_vs##_pe_##_pe##_main.attr.attr,				\
	&dp_tune_rate_##_rate##_vs_##_vs##_pe_##_pe##_pre.attr.attr,				\
	&dp_tune_rate_##_rate##_vs_##_vs##_pe_##_pe##_post.attr.attr,				\
	NULL											\
};												\
												\
static struct attribute_group dp_tune_rate_##_rate##_vs_##_vs##_pe_##_pe##_group = {		\
	.attrs = dp_tune_rate_##_rate##_vs_##_vs##_pe_##_pe##_attrs,				\
}

