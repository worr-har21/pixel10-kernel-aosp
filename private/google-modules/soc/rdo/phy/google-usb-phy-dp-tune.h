#define GOOGLE_USB_PHY_DEBUGFS_EQ_TUNE_ASIC_READ_LANE_N(_lane)					\
static int google_usb_phy_debugfs_eq_tune_asic_read_lane_##_lane##_main(void *data, u64 *val)	\
{												\
	struct google_usb_phy *gphy = data;							\
	mutex_lock(&gphy->lock);								\
	u16 reg, main;										\
												\
	if (gphy->phy_state != COMBO_PHY_TCA_READY) {						\
		*val = -EPERM;									\
		mutex_unlock(&gphy->lock);							\
		return 0;									\
	}											\
												\
	reg = readw(gphy->dptx_phy_regs_base + LANEN_DIG_ASIC_TX_ASIC_IN_1(_lane));		\
	main = ((reg & TX_MAIN_CURSOR_MASK) >> TX_MAIN_CURSOR_OFFSET) / 2;			\
	*val = main;										\
												\
	mutex_unlock(&gphy->lock);								\
	return 0;										\
}												\
												\
static int google_usb_phy_debugfs_eq_tune_asic_read_lane_##_lane##_pre(void *data, u64 *val)	\
{												\
	struct google_usb_phy *gphy = data;							\
	mutex_lock(&gphy->lock);								\
