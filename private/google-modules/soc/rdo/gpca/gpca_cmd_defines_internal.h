/* Word 3 */
#define START_CRYPTO_OP_PTRS_DATA_ADDR_MSB_FIELD GENMASK(7, 0)
#define START_CRYPTO_OP_PTRS_INPUT_DATA_SIZE_FIELD GENMASK(31, 8)
/* Word 4 */
#define START_CRYPTO_OP_PTRS_OUTPUT_DATA_ADDR_LSB_FIELD GENMASK(31, 0)
/* Word 5 */
#define START_CRYPTO_OP_PTRS_OUTPUT_DATA_ADDR_MSB_FIELD GENMASK(7, 0)
#define START_CRYPTO_OP_PTRS_OUTPUT_DATA_SIZE_FIELD GENMASK(31, 8)

#define START_CRYPTO_OP_PTRS_REQ_SIZE_WORDS 6
#define START_CRYPTO_OP_PTRS_MAX_UNENCRYPTED_SIZE_BYTES (64 * 1024 - 1)
