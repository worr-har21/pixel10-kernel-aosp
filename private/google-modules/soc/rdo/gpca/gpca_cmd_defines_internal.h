#define START_CRYPTO_OP_DATA_REQ_MAX_SIZE_WORDS                    \
	(START_CRYPTO_OP_DATA_AAD_WORD_OFFSET +                    \
	 BYTES_TO_WORDS(START_CRYPTO_OP_DATA_MAX_AAD_SIZE_BYTES) + \
	 BYTES_TO_WORDS(START_CRYPTO_OP_DATA_MAX_INPUT_SIZE_BYTES))

#define START_CRYPTO_OP_DATA_MAX_RSP_SIZE_WORDS 18

