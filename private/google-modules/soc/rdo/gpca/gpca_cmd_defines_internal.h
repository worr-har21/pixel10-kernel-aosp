/**
 * enum  gpca_cmd_iv_type - IV type.
 *
 * @GPCA_CMD_NO_IV: No IV for ECB mode.
 * @GPCA_CMD_12B_IV: 12 bytes IV for GCM mode.
 * @GPCA_CMD_16B_IV: 16 bytes IV for CTR/CBC mode.
 * @GPCA_CMD_8B_IV: 8 bytes IV for TDES CBC.
 */
enum gpca_cmd_iv_type {
	GPCA_CMD_NO_IV = 0,
	GPCA_CMD_12B_IV,
	GPCA_CMD_16B_IV,
	GPCA_CMD_8B_IV
};

