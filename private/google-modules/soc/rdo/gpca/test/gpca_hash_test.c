#define SIGN_OP GPCA_SUPPORTED_PURPOSE_SIGN
#define VERIFY_OP GPCA_SUPPORTED_PURPOSE_VERIFY
void gpca_crypto_sha_hmac_streaming(struct kunit *test)
{
	u32 i = 0;
	enum gpca_algorithm algo;
	dma_addr_t msg_dma_addr;
	dma_addr_t hash_dma_addr;
	u8 *msg_buf = NULL;
	u8 *hash_buf = NULL;
	u32 block_size = 0;
	u32 msg_size = 0;
	u32 digest_size = 0;
	u32 key_size = 0;
	struct gpca_key_handle *key_handle = NULL;
	struct gpca_crypto_op_handle *op_handle = NULL;
	struct gpca_key_policy kp = { GPCA_KEY_CLASS_PORTABLE,
				      GPCA_ALGO_HMAC_SHA2_224,
				      GPCA_SUPPORTED_PURPOSE_SIGN | GPCA_SUPPORTED_PURPOSE_VERIFY,
				      GPCA_KMK_DISABLE,
				      GPCA_WAEK_ENABLE,
				      GPCA_WAHK_DISABLE,
				      GPCA_WAPK_DISABLE,
				      GPCA_BS_DISABLE,
				      GPCA_EVICT_GSA_DISABLE,
				      GPCA_EVICT_AP_S_DISABLE,
				      GPCA_EVICT_AP_NS_DISABLE,
				      GPCA_OWNER_DOMAIN_ANDROID_VM,
				      GPCA_AUTH_DOMAIN_ANDROID_VM };

	op_handle = gpca_crypto_op_handle_alloc();
	KUNIT_EXPECT_PTR_NE(test, NULL, op_handle);

	for (i = 0; i < ARRAY_SIZE(gpca_streaming_digest_tests); i++) {
		algo = gpca_streaming_digest_tests[i].algo;
		kp.algo = algo;
		block_size = gpca_streaming_digest_tests[i].block_size;
		msg_size = gpca_streaming_digest_tests[i].msg_size;
		digest_size = gpca_streaming_digest_tests[i].digest_size;

		if (gpca_streaming_digest_tests[i].key) {
			key_size = gpca_streaming_digest_tests[i].key_size;
			if (gpca_ctx->drv_data->hw_bug_hmac_digest_size_keys) {
				switch (algo) {
				case GPCA_ALGO_HMAC_SHA2_224:
					key_size = HMAC_SHA224_DIGEST_SIZE;
					break;
				case GPCA_ALGO_HMAC_SHA2_256:
					key_size = HMAC_SHA256_DIGEST_SIZE;
					break;
				case GPCA_ALGO_HMAC_SHA2_384:
					key_size = HMAC_SHA384_DIGEST_SIZE;
					break;
				case GPCA_ALGO_HMAC_SHA2_512:
					key_size = HMAC_SHA512_DIGEST_SIZE;
					break;
				default:
					break;
				}
			}

			KUNIT_EXPECT_EQ_MSG(test, 0,
					    gpca_key_import(gpca_ctx, &key_handle, &kp,
							    gpca_streaming_digest_tests[i].key,
							    key_size
							    ),
					    "Key import failed for iteration = %d", i);
			KUNIT_EXPECT_EQ_MSG(test, 0,
					    gpca_crypto_set_hmac_params_sync(gpca_ctx,
									     op_handle,
									     key_handle,
									     algo,
									     SIGN_OP),
				"Set crypto params failed for iteration = %d", i);
		} else {
			KUNIT_EXPECT_EQ_MSG(test, 0,
					    gpca_crypto_set_hash_params_sync(gpca_ctx,
									     op_handle, algo),
					    "Set crypto params failed for iteration = %d", i);
		}
		msg_buf = kmalloc(msg_size, GFP_KERNEL);
		hash_buf = kmalloc(digest_size, GFP_KERNEL);

		memcpy(msg_buf, gpca_streaming_digest_tests[i].msg, msg_size);

		/* DMA map */
		msg_dma_addr = dma_map_single(gpca_ctx->dev, msg_buf,
					      msg_size,
					      DMA_TO_DEVICE);
		hash_dma_addr = dma_map_single(gpca_ctx->dev, hash_buf,
					       digest_size,
					       DMA_FROM_DEVICE);
		KUNIT_EXPECT_EQ_MSG(test, 0,
				    gpca_crypto_start_digest_op_sync(gpca_ctx,
								     op_handle,
								     msg_dma_addr,
								     block_size,
								     0, 0,
								     false),
				    "Start crypto op(ptrs) failed for iteration = %d", i);
		KUNIT_EXPECT_EQ_MSG(test, 0,
				    gpca_crypto_start_digest_op_sync(gpca_ctx,
								     op_handle,
								     msg_dma_addr + block_size,
								     msg_size - block_size,
								     hash_dma_addr,
								     digest_size,
								     true),
				    "Start crypto op(ptrs) last = 1 failed for iteration = %d", i);

		/* DMA Unmap */
		dma_unmap_single(gpca_ctx->dev, msg_dma_addr, msg_size, DMA_TO_DEVICE);
		dma_unmap_single(gpca_ctx->dev, hash_dma_addr, digest_size, DMA_FROM_DEVICE);
		/* Clear op slot */
		KUNIT_EXPECT_EQ_MSG(test, 0,
				    gpca_crypto_clear_op_sync(gpca_ctx, op_handle),
				    "Clear op slot failed for iteration = %d",
				    i);
		/* Compare hash */
		KUNIT_EXPECT_EQ_MSG(test, 0,
				    memcmp(hash_buf, gpca_streaming_digest_tests[i].digest,
					   digest_size),
				    "Hash didn't match for iteration = %d", i);

		kfree(hash_buf);
		kfree(msg_buf);

		if (gpca_streaming_digest_tests[i].key) {
			/* Test HMAC verify streaming */
			KUNIT_EXPECT_EQ_MSG(test, 0,
					    gpca_crypto_set_hmac_params_sync(gpca_ctx, op_handle,
									     key_handle, algo,
									     VERIFY_OP),
					    "Set crypto params failed for iteration = %d", i);
			msg_buf = kmalloc(msg_size + digest_size, GFP_KERNEL);
			memcpy(msg_buf, gpca_streaming_digest_tests[i].msg, msg_size);
			memcpy(msg_buf + msg_size, gpca_streaming_digest_tests[i].digest,
			       digest_size);

			/* DMA map */
			msg_dma_addr = dma_map_single(gpca_ctx->dev, msg_buf,
						      msg_size + digest_size,
						      DMA_TO_DEVICE);
			KUNIT_EXPECT_EQ_MSG(test, 0,
					    gpca_crypto_start_verify_op_sync(gpca_ctx, op_handle,
									     msg_dma_addr,
									     block_size,
									     0, false),
					    "Start verify op failed for iteration = %d", i);
			KUNIT_EXPECT_EQ_MSG(test, 0,
					    gpca_crypto_start_verify_op_sync(gpca_ctx, op_handle,
									     msg_dma_addr
										+ block_size,
									     msg_size - block_size,
									     digest_size,
									     true),
					    "Start verify op last = 1 failed for iteration = %d",
					    i);

			/* DMA Unmap */
			dma_unmap_single(gpca_ctx->dev, msg_dma_addr, msg_size + digest_size,
					 DMA_TO_DEVICE);
			/* Clear op slot */
			KUNIT_EXPECT_EQ_MSG(test, 0, gpca_crypto_clear_op_sync(gpca_ctx, op_handle),
					    "Clear op slot failed for iteration = %d", i);
		}
		/* Clear Key */
		if (gpca_streaming_digest_tests[i].key)
			KUNIT_EXPECT_EQ_MSG(test, 0,
					    gpca_key_clear(gpca_ctx, &key_handle),
					    "Clear key failed for iteration = %d", i);
	}
	gpca_crypto_op_handle_free(op_handle);
}

