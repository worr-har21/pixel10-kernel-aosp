void gpca_crypto_sha(struct kunit *test)
{
	u32 i = 0, j = 0;
	enum gpca_algorithm algo;
	dma_addr_t msg_dma_addr;
	dma_addr_t hash_dma_addr;
	u8 *msg_buf = NULL;
	u8 *hash_buf = NULL;
	struct gpca_crypto_op_handle *op_handle = NULL;

	op_handle = gpca_crypto_op_handle_alloc();
	KUNIT_EXPECT_PTR_NE(test, NULL, op_handle);

	for (i = 0; i < ARRAY_SIZE(sha_tests); i++) {
		algo = sha_tests[i].algo;
		KUNIT_EXPECT_EQ_MSG(test, 0,
				    gpca_crypto_set_hash_params_sync(gpca_ctx,
								     op_handle,
								     algo),
			"Set crypto params failed for iteration = %d", i);

		msg_buf = kmalloc(sha_tests[i].msg_size, GFP_KERNEL);
		hash_buf = kmalloc(sha_tests[i].hash_size, GFP_KERNEL);

		for (j = 0; j < sha_tests[i].msg_size; j++)
			msg_buf[j] = sha_tests[i].msg[j];

		/* DMA map */
		msg_dma_addr = dma_map_single(gpca_ctx->dev, msg_buf,
					      sha_tests[i].msg_size,
					      DMA_TO_DEVICE);
		hash_dma_addr = dma_map_single(gpca_ctx->dev, hash_buf,
					       sha_tests[i].hash_size,
					       DMA_FROM_DEVICE);

		KUNIT_EXPECT_EQ_MSG(test, 0,
				    gpca_crypto_start_digest_op_sync(gpca_ctx,
								     op_handle,
								     msg_dma_addr,
								     sha_tests[i].msg_size,
								     hash_dma_addr,
								     sha_tests[i].hash_size,
								     true),
			"Start crypto op(ptrs) failed for iteration = %d", i);
		/* DMA Unmap */
		dma_unmap_single(gpca_ctx->dev, msg_dma_addr,
				 sha_tests[i].msg_size, DMA_TO_DEVICE);
		dma_unmap_single(gpca_ctx->dev, hash_dma_addr,
				 sha_tests[i].hash_size, DMA_FROM_DEVICE);
		/* Clear op slot */
		KUNIT_EXPECT_EQ_MSG(test, 0,
				    gpca_crypto_clear_op_sync(gpca_ctx, op_handle),
				    "Clear op slot failed for iteration = %d",
				    i);
		/* Compare hash */
		for (j = 0; j < sha_tests[i].hash_size; j++)
			KUNIT_EXPECT_EQ_MSG(test, hash_buf[j], sha_tests[i].hash[j],
					    "Hash didn't match for iteration %d at index %d, expected = 0x%x actual = 0x%x",
					    i, j, hash_buf[j], sha_tests[i].hash[j]);
		kfree(msg_buf);
		kfree(hash_buf);
	}
	gpca_crypto_op_handle_free(op_handle);
}

